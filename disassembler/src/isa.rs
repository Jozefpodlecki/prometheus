#[derive(Debug, Clone, PartialEq, Eq)]
pub struct MemoryAccess {
    pub segment: Option<Segment>,
    pub base: Option<Register>,
    pub index: Option<Register>,
    pub scale: u8,
    pub displacement: i64,
    pub size: u8,
    /* AVX-512 Broadcast semantics. True if reading a single element and broadcasting it to the vector. */
    pub broadcast: bool,
    /* Calculated absolute address for RIP-relative memory addressing. */
    pub absolute_address: Option<u64>,
}

#[derive(Debug, Clone, Copy, PartialEq, Eq)]
pub enum Segment {
    CS, DS, ES, FS, GS, SS,
}

#[derive(Debug, Clone, Copy, PartialEq, Eq)]
pub struct InstructionMetadata {
    pub length: u8,
    pub architecture: Architecture,
    pub category: InstructionCategory,
    pub extension: IsaExtension,
    pub control_flow: ControlFlow,
    pub flags: FlagEffect,
    pub attributes: Attributes,
}

#[derive(Debug, Clone, Copy, PartialEq, Eq)]
pub enum IsaExtension {
    Base,
    SSE, SSE2, SSE3, SSSE3, SSE4_1, SSE4_2,
    AVX, AVX2, AVX512,
    AmxTile, AmxInt8, AmxBf16,
    BMI1, BMI2,
    AES, PCLMULQDQ,
    APX, XOP, TSX, CET,
}

/*
** Instruction attributes provide high-level properties of the instruction
** that are useful for static analysis without inspecting the operands.
*/
#[derive(Debug, Clone, Copy, PartialEq, Eq, Default)]
pub struct Attributes {
    pub is_stack_op: bool,
    pub is_string_op: bool,
    pub is_vector_op: bool,
    pub is_privileged: bool,
    pub is_far_branch: bool,
    pub has_lock: bool,
    pub has_rep: bool,
    pub has_repne: bool,
    pub has_xacquire: bool,
    pub has_xrelease: bool,
    pub sae: bool,
    pub rounding_mode: Option<RoundingMode>,
}

#[derive(Debug, Clone, Copy, PartialEq, Eq)]
pub enum RoundingMode {
    RoundNearest,
    RoundDown,
    RoundUp,
    RoundZero,
}

#[derive(Debug, Clone, Copy, PartialEq, Eq, Default)]
pub struct FlagEffect {
    /* Flags that are read/tested by the instruction. */
    pub tested: u32,
    /* Flags whose value is calculated/modified by the instruction. */
    pub modified: u32,
    /* Flags that are always set to 1. */
    pub set: u32,
    /* Flags that are always cleared to 0. */
    pub cleared: u32,
    /* Flags whose state is undefined after execution. */
    pub undefined: u32,
}

#[derive(Debug, Clone, Copy, PartialEq, Eq)]
pub enum ControlFlow {
    None,
    ConditionalBranch,
    UnconditionalBranch,
    Call,
    Return,
    Interrupt,
    Syscall,
}

/*
** Bitmask for CPU status flags (EFLAGS/RFLAGS).
*/
pub mod flags {
    pub const CF: u32 = 1 << 0;  // Carry Flag
    pub const PF: u32 = 1 << 2;  // Parity Flag
    pub const AF: u32 = 1 << 4;  // Auxiliary Carry Flag
    pub const ZF: u32 = 1 << 6;  // Zero Flag
    pub const SF: u32 = 1 << 7;  // Sign Flag
    pub const TF: u32 = 1 << 8;  // Trap Flag
    pub const IF: u32 = 1 << 9;  // Interrupt Enable Flag
    pub const DF: u32 = 1 << 10; // Direction Flag
    pub const OF: u32 = 1 << 11; // Overflow Flag
}

#[derive(Debug, Clone, Copy, PartialEq, Eq)]
pub enum Architecture {
    X86,
    X64,
}

#[derive(Debug, Clone, Copy, PartialEq, Eq)]
pub enum InstructionCategory {
    Arithmetic,
    DataTransfer,
    ControlFlow,
    System,
    Miscellaneous,
}
