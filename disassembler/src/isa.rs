/*
** Instruction represents a fully decoded x86/x64 instruction.
** It contains the raw bytes, prefixes, and high-level semantic
** information required for static analysis.
*/
#[derive(Debug, Clone, PartialEq, Eq)]
pub struct Instruction {
    /* The virtual address where the instruction is located. */
    pub address: u64,
    /* The full sequence of bytes forming the instruction. */
    pub bytes: Vec<u8>,
    /* The legacy and REX prefixes detected during decoding. */
    pub prefixes: Vec<u8>,
    /* The primary instruction mnemonic. */
    pub mnemonic: Mnemonic,
    /* Explicit, implicit, and hidden operands. */
    pub operands: Vec<Operand>,
    /* Architecture-specific classification and flag effects. */
    pub metadata: InstructionMetadata,
    /* Detailed breakdown of the instruction's byte segments. */
    pub segments: InstructionSegments,
}

/*
** InstructionSegments identifies the byte offsets and lengths of each
** structural component of the instruction.
*/
#[derive(Debug, Clone, Copy, PartialEq, Eq, Default)]
pub struct InstructionSegments {
    pub prefixes: SegmentInfo,
    pub opcode: SegmentInfo,
    pub modrm: SegmentInfo,
    pub sib: SegmentInfo,
    pub displacement: SegmentInfo,
    pub immediate: SegmentInfo,
}

#[derive(Debug, Clone, Copy, PartialEq, Eq, Default)]
pub struct SegmentInfo {
    pub offset: u8,
    pub length: u8,
}

use crate::autogen_isa::AutoMnemonic;

#[derive(Debug, Clone, Copy, PartialEq, Eq)]
pub enum Mnemonic {
    Add, Sub, Mov, Push, Pop, Ret, Call, Jmp, Nop, Syscall,
    Cmp, Xor, And, Or, Adc, Sbb,
    Jz, Jnz, Js, Jns, Jo, Jno, Jb, Jae,
    Lea, Int,
    Test, Inc, Dec,
    Pushfq, Popfq,
    Clc, Stc, Cld, Std,
    Vmovaps, Vaddps, Vsubps,
    Aesenc, Aesdec,
    Movs,
    Ldtilecfg, Sttilecfg, Tdpbf16ps,
    Vprotb, // XOP example
    Fadd, Fsub, Fmul, Fdiv, // x87 examples
    Endbr64, // CET example
    Xadd, Cmpxchg, Neg, Not, Btc, Btr, Bts, Xchg,
    Unknown,
    Auto(AutoMnemonic), // Autogenerated mapping integration
}

#[derive(Debug, Clone, PartialEq, Eq)]
pub enum Operand {
    Register {
        reg: Register,
        access: AccessType,
        visibility: Visibility,
        /* AVX-512 Opmask applied to this destination register (e.g. {k1}) */
        opmask: Option<Register>, 
        /* AVX-512 Zeroing behavior on inactive elements ({z}) */
        zeroing: bool,
    },
    Immediate {
        imm: Immediate,
        visibility: Visibility,
    },
    Memory {
        mem: MemoryAccess,
        access: AccessType,
        visibility: Visibility,
        opmask: Option<Register>,
        zeroing: bool,
    },
}

#[derive(Debug, Clone, Copy, PartialEq, Eq)]
pub enum Visibility {
    /* Operand is explicitly encoded in the instruction bytes. */
    Explicit,
    /* Operand is part of the instruction semantics but not encoded. */
    Implicit,
    /* Operand is used internally and not usually shown in disassembly. */
    Hidden,
}

#[derive(Debug, Clone, Copy, PartialEq, Eq)]
pub enum AccessType {
    Read,
    Write,
    ReadWrite,
    None,
}

