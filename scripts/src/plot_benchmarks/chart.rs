use anyhow::Result;
use plotters::style::text_anchor::{HPos, VPos};
use plotters::{prelude::*, style::text_anchor::Pos};
use plotters::coord::types::RangedCoordf64;
use tracing::info;

use crate::plot_benchmarks::{
    config::BenchmarkConfig,
    models::ChartData,
};

const DARK_BG: RGBColor = RGBColor(18, 18, 18);
const DARK_GRID: RGBColor = RGBColor(64, 64, 64);
const DARK_TEXT: RGBColor = RGBColor(220, 220, 220);
const DARK_AXIS: RGBColor = RGBColor(100, 100, 100);

const PALETTE: [RGBColor; 6] = [
    RGBColor(41, 128, 185),   // Deep Blue
    RGBColor(231, 76, 60),    // Soft Red
    RGBColor(46, 204, 113),   // Emerald Green
    RGBColor(241, 196, 15),   // Sunflower Yellow
    RGBColor(155, 89, 182),   // Purple
    RGBColor(52, 152, 219),   // Light Blue
];

pub struct ChartGenerator {
    config: BenchmarkConfig,
}

impl ChartGenerator {
    pub fn new(config: BenchmarkConfig) -> Self {
        Self { config }
    }
    
    pub fn generate(&self, data: &ChartData) -> Result<()> {
        info!("Generating benchmark chart");
        
        let root = BitMapBackend::new(
            &self.config.output_path,
            (self.config.chart_width, self.config.chart_height),
        )
        .into_drawing_area();
        
        root.fill(&DARK_BG)?;
        
        let max_y = data.max_value();
        let y_max_adjusted = (max_y * 1.15).max(10.0);
        
        let mut chart = ChartBuilder::on(&root)
            .caption(
                "Disassembly Performance Comparison (Higher is Better)",
                ("sans-serif", 28).into_font().color(&DARK_TEXT),
            )
            .margin_top(40)
            .margin_right(60)
            .margin_bottom(30)
            .margin_left(80)
            .x_label_area_size(60)
            .y_label_area_size(60)
            .build_cartesian_2d(
                0f64..data.benchmarks.len() as f64,
                0f64..y_max_adjusted,
            )?;
        
        chart
            .configure_mesh()
            .y_desc("Decode Throughput (MiB/s)")
            .x_desc("")
            .axis_style(&DARK_AXIS)
            .label_style(("sans-serif", 12).into_font().color(&DARK_TEXT))
            .bold_line_style(&DARK_GRID)
            .light_line_style(&DARK_GRID)
            .x_labels(data.benchmarks.len())
            .x_label_formatter(&|x| {
                let idx = *x as usize;
                if idx < data.benchmarks.len() {
                    data.benchmarks[idx].replace('_', " ")
                } else {
                    String::new()
                }
            })
            .y_label_formatter(&|y| format!("{:.1}", y))
            .draw()?;
        
        self.draw_bars(&mut chart, data)?;
        self.draw_labels(&mut chart, data, y_max_adjusted)?;
        self.draw_value_lines(&mut chart, data)?;
        
        chart
            .configure_series_labels()
            .background_style(&DARK_BG.mix(0.0))
            .border_style(&DARK_AXIS)
            .label_font(("sans-serif", 13).into_font().color(&DARK_TEXT))
            .position(SeriesLabelPosition::UpperRight)
            .draw()?;
        
        let (width, height) = root.dim_in_pixel();
        let watermark_style = TextStyle::from(("sans-serif", 10).into_font())
            .color(&DARK_GRID)
            .pos(Pos::new(HPos::Left, VPos::Bottom));
        root.draw_text(
            "Generated by Benchmark Suite",
            &watermark_style,
            ((width - 180) as i32, (height - 15) as i32),
        )?;
        
        root.present()?;
        
        info!("Chart saved to: {}", self.config.output_path.display());
        
        Ok(())
    }
    
    fn draw_bars(
        &self,
        chart: &mut ChartContext<'_, BitMapBackend, Cartesian2d<RangedCoordf64, RangedCoordf64>>,
        data: &ChartData,
    ) -> Result<()> {
        let bar_spacing = 0.05;
        let group_width = 1.0 - bar_spacing * 2.0;
        let bar_width = group_width / data.engines.len() as f64;
        let start_offset = bar_spacing;
        
        for (engine_idx, engine) in data.engines.iter().enumerate() {
            let color = PALETTE[engine_idx % PALETTE.len()];
            
            let lighter_color = RGBColor(
                (color.0 as f64 * 0.7).min(255.0) as u8,
                (color.1 as f64 * 0.7).min(255.0) as u8,
                (color.2 as f64 * 0.7).min(255.0) as u8,
            );
            
            let mut bar_elements = Vec::new();
            
            for (bench_idx, row) in data.values.iter().enumerate() {
                let x_center = bench_idx as f64 + start_offset + engine_idx as f64 * bar_width;
                let x_left = x_center;
                let x_right = x_center + bar_width;
                let y = row[engine_idx];
                
                if y > 0.0 {
                    let shadow_rect = Rectangle::new(
                        [(x_left + 2.0, 2.0), (x_right + 2.0, y + 2.0)],
                        DARK_GRID.mix(0.3).filled(),
                    );
                    
                    let bar_rect = Rectangle::new(
                        [(x_left, 0.0), (x_right, y)],
                        color.filled(),
                    );
                    
                    let highlight_rect = Rectangle::new(
                        [(x_left, y - y * 0.05), (x_right, y)],
                        lighter_color.filled(),
                    );
                    
                    let group = (shadow_rect, bar_rect, highlight_rect);
                    bar_elements.push(group);
                }
            }
            
            for (shadow_rect, bar_rect, highlight_rect) in bar_elements {
                chart.draw_series(std::iter::once(shadow_rect))?;
                chart.draw_series(std::iter::once(bar_rect))?;
                chart.draw_series(std::iter::once(highlight_rect))?;
            }
            
            chart.draw_series(std::iter::empty::<Rectangle<(f64, f64)>>())?
                .label(engine.clone())
                .legend(move |(x, y)| Rectangle::new([(x, y - 8), (x + 16, y + 4)], color.filled()));
        }
        
        Ok(())
    }
    
    fn draw_labels(
        &self,
        chart: &mut ChartContext<'_, BitMapBackend, Cartesian2d<RangedCoordf64, RangedCoordf64>>,
        data: &ChartData,
        max_y: f64,
    ) -> Result<()> {
        let bar_spacing = 0.05;
        let group_width = 1.0 - bar_spacing * 2.0;
        let bar_width = group_width / data.engines.len() as f64;
        let start_offset = bar_spacing;
        
        for (engine_idx, _) in data.engines.iter().enumerate() {
            for (bench_idx, row) in data.values.iter().enumerate() {
                let value = row[engine_idx];
                
                if value > 0.0 {
                    let x_center = bench_idx as f64 + start_offset + engine_idx as f64 * bar_width;
                    let label_x = x_center + bar_width / 2.0;
                    let label_y = value + (max_y * 0.02).max(2.0);
                    
                    chart.draw_series(std::iter::once(Text::new(
                        format!("{:.1}", value),
                        (label_x, label_y),
                        ("sans-serif", 12).into_font().color(&DARK_TEXT),
                    )))?;
                }
            }
        }
        
        Ok(())
    }
    
    fn draw_value_lines(
        &self,
        chart: &mut ChartContext<'_, BitMapBackend, Cartesian2d<RangedCoordf64, RangedCoordf64>>,
        data: &ChartData,
    ) -> Result<()> {
        let max_val = data.max_value();
        let step = if max_val > 100.0 {
            50.0
        } else if max_val > 50.0 {
            25.0
        } else if max_val > 20.0 {
            10.0
        } else if max_val > 10.0 {
            5.0
        } else {
            2.0
        };
        
        let mut current = step;
        while current <= max_val {
            let line_color = DARK_GRID.mix(0.5);
            chart.draw_series(std::iter::once(PathElement::new(
                vec![(0.0, current), (data.benchmarks.len() as f64, current)],
                line_color.stroke_width(1),
            )))?;
            
            chart.draw_series(std::iter::once(Text::new(
                format!("{:.0}", current),
                (data.benchmarks.len() as f64 + 0.05, current),
                ("sans-serif", 9).into_font().color(&DARK_GRID),
            )))?;
            
            current += step;
        }
        
        Ok(())
    }
}